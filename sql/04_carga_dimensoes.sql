WHERE COD_PRODUTO IS NOT NULL;
COMMIT;

INSERT INTO DIM_LOCAL (COD_CIDADE, NOM_CIDADE, COD_ESTADO, NOM_ESTADO, COD_PAIS, NOM_PAIS)
