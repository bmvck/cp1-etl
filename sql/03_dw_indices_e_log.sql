BEGIN
    EXECUTE IMMEDIATE 'DROP TABLE LOG_ETL CASCADE CONSTRAINTS';
EXCEPTION
    WHEN OTHERS THEN
        IF SQLCODE != -942 THEN RAISE; END IF;
END;
/

CREATE TABLE LOG_ETL (
    ID_LOG        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOM_PROCESSO  VARCHAR2(100) NOT NULL,
    DAT_EXECUCAO  TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    QTD_REGISTROS NUMBER,
    STA_STATUS    VARCHAR2(20) NOT NULL,
    DES_MENSAGEM  VARCHAR2(4000)
);

CREATE OR REPLACE PROCEDURE SP_REGISTRA_LOG_ETL (
    P_NOM_PROCESSO  IN VARCHAR2,
    P_QTD_REGISTROS IN NUMBER,
    P_STA_STATUS    IN VARCHAR2,
    P_DES_MENSAGEM  IN VARCHAR2
) IS
    PRAGMA AUTONOMOUS_TRANSACTION;
BEGIN
    INSERT INTO LOG_ETL (
        NOM_PROCESSO,
        QTD_REGISTROS,
        STA_STATUS,
        DES_MENSAGEM
    ) VALUES (
        P_NOM_PROCESSO,
        P_QTD_REGISTROS,
        P_STA_STATUS,
        SUBSTR(P_DES_MENSAGEM, 1, 4000)
    );

    COMMIT;
END;
