BEGIN EXECUTE IMMEDIATE 'DROP TABLE FATO_VENDA CASCADE CONSTRAINTS'; EXCEPTION WHEN OTHERS THEN IF SQLCODE != -942 THEN RAISE; END IF; END;
/
CREATE TABLE FATO_VENDA (
    SK_FATO_VENDA NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    COD_PEDIDO NUMBER(10),
    COD_ITEM_PEDIDO NUMBER,
    SK_TEMPO NUMBER,
    SK_CLIENTE NUMBER,
    SK_VENDEDOR NUMBER,
    SK_PRODUTO NUMBER,
    SK_LOCAL NUMBER,
    QTD_ITEM NUMBER(10),
    VAL_UNITARIO NUMBER(12,2),
    VAL_DESCONTO NUMBER(12,2),
    VAL_TOTAL_ITEM NUMBER(12,2),
    CONSTRAINT FK_FATO_TEMPO FOREIGN KEY (SK_TEMPO) REFERENCES DIM_TEMPO(SK_TEMPO),
    CONSTRAINT FK_FATO_CLIENTE FOREIGN KEY (SK_CLIENTE) REFERENCES DIM_CLIENTE(SK_CLIENTE),
    CONSTRAINT FK_FATO_VENDEDOR FOREIGN KEY (SK_VENDEDOR) REFERENCES DIM_VENDEDOR(SK_VENDEDOR),
    CONSTRAINT FK_FATO_PRODUTO FOREIGN KEY (SK_PRODUTO) REFERENCES DIM_PRODUTO(SK_PRODUTO),
    CONSTRAINT FK_FATO_LOCAL FOREIGN KEY (SK_LOCAL) REFERENCES DIM_LOCAL(SK_LOCAL)
);
