BEGIN EXECUTE IMMEDIATE 'DROP TABLE DIM_TEMPO CASCADE CONSTRAINTS'; EXCEPTION WHEN OTHERS THEN IF SQLCODE != -942 THEN RAISE; END IF; END;
/
CREATE TABLE DIM_TEMPO (
    SK_TEMPO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DATA_COMPLETA DATE,
    DIA NUMBER,
    MES NUMBER,
    ANO NUMBER,
    NOME_MES VARCHAR2(20)
);

BEGIN EXECUTE IMMEDIATE 'DROP TABLE DIM_CLIENTE CASCADE CONSTRAINTS'; EXCEPTION WHEN OTHERS THEN IF SQLCODE != -942 THEN RAISE; END IF; END;
/
CREATE TABLE DIM_CLIENTE (
    SK_CLIENTE NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    COD_CLIENTE NUMBER(10),
    NOM_CLIENTE VARCHAR2(50),
    DES_RAZAO_SOCIAL VARCHAR2(80),
    TIP_PESSOA CHAR(1),
    STA_ATIVO CHAR(1)
);

BEGIN EXECUTE IMMEDIATE 'DROP TABLE DIM_VENDEDOR CASCADE CONSTRAINTS'; EXCEPTION WHEN OTHERS THEN IF SQLCODE != -942 THEN RAISE; END IF; END;
/
CREATE TABLE DIM_VENDEDOR (
    SK_VENDEDOR NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    COD_VENDEDOR NUMBER(4),
    NOM_VENDEDOR VARCHAR2(50),
    STA_ATIVO CHAR(1)
);

BEGIN EXECUTE IMMEDIATE 'DROP TABLE DIM_PRODUTO CASCADE CONSTRAINTS'; EXCEPTION WHEN OTHERS THEN IF SQLCODE != -942 THEN RAISE; END IF; END;
/
CREATE TABLE DIM_PRODUTO (
    SK_PRODUTO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    COD_PRODUTO NUMBER(10),
    NOM_PRODUTO VARCHAR2(50),
    COD_BARRA VARCHAR2(20),
    STA_ATIVO VARCHAR2(20)
);

BEGIN EXECUTE IMMEDIATE 'DROP TABLE DIM_LOCAL CASCADE CONSTRAINTS'; EXCEPTION WHEN OTHERS THEN IF SQLCODE != -942 THEN RAISE; END IF; END;
/
CREATE TABLE DIM_LOCAL (
    SK_LOCAL NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    COD_CIDADE NUMBER(6),
    NOM_CIDADE VARCHAR2(50),
    COD_ESTADO CHAR(3),
    NOM_ESTADO VARCHAR2(50),
    COD_PAIS NUMBER(3),
    NOM_PAIS VARCHAR2(50)
);
